// Оператори присвоювання
let age = 18 + 5
age += 2 // age = age + 2
age -= 3 //age = age - 3
age *= 2 //age = age + 2
age /= 2 // age = age / 2

age++ // age = age + 1
age-- // age = age - 1

console.log(age)

// Оператори порівняння
const Peter = 20
console.log(age > Peter)
console.log(age >= Peter)
console.log(age < Peter)
console.log(age <= Peter)
console.log(age == Peter)
